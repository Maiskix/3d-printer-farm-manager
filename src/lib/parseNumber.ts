// Безопасный парсинг чисел из пользовательского ввода.
// Поддерживает запятую как десятичный разделитель, обрезает пробелы,
// и никогда не возвращает NaN — при некорректном вводе отдаёт значение по умолчанию.
export function число(значение: unknown, поУмолчанию = 0): number {
  if (значение === null || значение === undefined) return поУмолчанию;

  const строка = String(значение).trim().replace(',', '.');
  if (строка === '') return поУмолчанию;

  const результат = Number(строка);
  return Number.isFinite(результат) ? результат : поУмолчанию;
}
